CREATE TABLE users (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  username VARCHAR(64) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  name VARCHAR(64) NOT NULL,
  role VARCHAR(32) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE data_assets (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  asset_code VARCHAR(64) NOT NULL UNIQUE,
  name VARCHAR(128) NOT NULL,
  owner VARCHAR(128) NOT NULL,
  sensitivity VARCHAR(32) NOT NULL,
  status VARCHAR(32) NOT NULL,
  price DECIMAL(12,2) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE auth_requests (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  applicant VARCHAR(128) NOT NULL,
  asset_name VARCHAR(128) NOT NULL,
  status VARCHAR(32) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE audit_logs (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  action VARCHAR(128) NOT NULL,
  actor VARCHAR(128) NOT NULL,
  result VARCHAR(32) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE blockchain_records (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  tx_hash VARCHAR(128) NOT NULL,
  action VARCHAR(128) NOT NULL,
  detail VARCHAR(255) NOT NULL,
  actor VARCHAR(128) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE privacy_tasks (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  task_name VARCHAR(128) NOT NULL,
  status VARCHAR(32) NOT NULL,
  progress INT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE finance_metrics (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  electricity_price DECIMAL(10,2) NOT NULL,
  cloud_cost DECIMAL(12,2) NOT NULL,
  data_cost DECIMAL(12,2) NOT NULL,
  roi DECIMAL(10,2) NOT NULL,
  payback_months DECIMAL(10,2) NOT NULL,
  carbon_reduction DECIMAL(10,2) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
